package Modelo;

/**
 *
 * @author blass
 */
public class Ciudad {
    int id;
    int id_pais;
    String nombre;

    public Ciudad() {
    }

    public Ciudad(int id, int id_pais, String nombre) {
        this.id = id;
        this.id_pais = id_pais;
        this.nombre = nombre;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getId_pais() {
        return id_pais;
    }

    public void setId_pais(int id_pais) {
        this.id_pais = id_pais;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    
}
